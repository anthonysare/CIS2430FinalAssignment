    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        if (!blank.isEmpty()) {
            blank.clear();
        }
        blank.add(" ");
        CardLayout card = (CardLayout) mainPanel.getLayout();
        card.show(mainPanel, "studentCard");

        temp = mc.loadStudent(loginStuNumField.getText(), loginNameField.getText());

        if (temp.getId() != null) {
            System.out.println("Succesful Login for " + temp.getName() + "\n");
        } else {
            temp = new DBStudent(loginStuNumField.getText(), loginNameField.getText());
            temp.setCourses(blank);
            mc.saveStudent(temp);
            System.out.println("New user created");
        }

        studentNameField.setText(temp.getName());
        if (temp.getDegree().equals("CS") || temp.getDegree().equals("SEng")) {
            studentDegreeField.setText("Honours Degree");
            studentDegree = 'H';
        } else if (temp.getDegree().equals("THST") || temp.getDegree().equals("BCG")) {
            studentDegreeField.setText("General Degree");
        } else {
            studentDegreeField.setText("NA");
        }


    }//GEN-LAST:event_loginButtonActionPerformed


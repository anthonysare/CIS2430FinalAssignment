    public String[] pos(HashMap<Course, String> pos) {
        ArrayList<String> plan = new ArrayList();
        for (Map.Entry<Course, String> plannedCourseList : pos.entrySet()) {
            Course key = plannedCourseList.getKey();
            String value = plannedCourseList.getValue();
            plan.add(key.getCourseCode() + "-" + key.getCourseTitle() + "-" + value);
        }
        String[] posString = (String[]) plan.toArray(new String[plan.size()]);
        return posString;
    }

    public String[] transcript(ArrayList<Attempt> attemptList) {
        ArrayList<String> transcript = new ArrayList();
        for (Attempt a : attemptList) {
            transcript.add(a.getCourseAttempted().getCourseCode() + "-" + a.getCourseAttempted().getCourseTitle() + "-" + a.getSemesterTaken() + "-" + a.getAttemptGrade());
        }
        String[] transString = (String[]) transcript.toArray(new String[transcript.size()]);
        return transString;
    }

